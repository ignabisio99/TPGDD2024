			DETALLE_TICKET_ID INTEGER REFERENCES [QUICK_SORT].[PROMOCION],
			PROMO_CODIGO INTEGER REFERENCES [QUICK_SORT].[PROMOCION],
			PROMO_TOTAL DECIMAL(18,2),
